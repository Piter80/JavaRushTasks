package com.javarush.task.task03.task0308;

/* 
Произведение 10 чисел
*/

public class Solution {
    public static void main(String[] args) {
        int pr = 1;

        for (int i = 1; i <= 10; i++) {
            pr = pr * i;
        }

        System.out.println(pr);
        //напишите тут ваш код
    }
}
